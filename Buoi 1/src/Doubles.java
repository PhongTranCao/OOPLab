import java.util.Scanner;

public class Doubles {
    public static void main(String[] args) {
        Scanner s = new Scanner(System.in);
        Double x1 = s.nextDouble(), x2 = s.nextDouble();
        System.out.println(x1 + x2);
        System.out.println(x1/x2);
        System.out.println(x1 - x2);
        System.out.println(x1 * x2);
    }
}
